"""Static analysis of a 16 MB A100 PCIe 80GB BAR0 dump.

Reads a 16 MB raw BAR0 image (the kind you get from
``dd if=/sys/bus/pci/devices/0000:01:00.0/resource0 of=/tmp/dump.bin``)
and produces a structured report:

  * Family A range (0x110000-0x113000): non-zero registers
  * Family B range (0x120000-0x123000): non-zero registers
  * Family A vs cmpunlocker 10GB CMP baseline: diffs
  * Family B vs cmpunlocker 10GB CMP baseline: diffs (the 80GB candidates)
  * Suggested cfg1/lmr candidates (heuristic ranking)
  * 0xbadf5040 / 0xbadf1100 / 0xbadf1300 (NVIDIA debug-marker) — never
    actual 80GB values, just markers
  * Reserved / write-protected ranges

The output is a YAML report ready to be pasted into a
constants.yaml or handed to whoever has root on a CMP 170HX.

Usage:
    python3 -m tools.a100_register_report a100-0000_01_00_0-bar0-16m.bin
"""

