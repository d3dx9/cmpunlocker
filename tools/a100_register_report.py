# NVIDIA debug markers — never actual values, just firmware-scratch.
NVIDIA_DEBUG_MARKERS = (0xbadf5040, 0xbadf1100, 0xbadf1300)


def is_marker(val):
    return any((val & m) == m for m in NVIDIA_DEBUG_MARKERS)


def load_bar0(path):
    with open(path, 'rb') as f:
        return f.read()


def dump_range(data, lo, hi, step=4, cmp10gb=None, label='?'):
    """Return list of (addr, value) where value != 0, with diff vs cmp10gb."""
    out = []
    for off in range(lo, hi, step):
        if off + 4 > len(data):
            break
        val = struct.unpack_from('<I', data, off)[0]
        if val != 0:
            cmp_v = cmp10gb.get(off, None) if cmp10gb else None
            differs = (cmp_v is not None and cmp_v != val)
            out.append((off, val, cmp_v, differs))
    return out


def report_yaml(out, dump_path):
    """Pretty-print the report in YAML for constants.yaml consumption."""
    fam_a_entries = out['fam_a']
    fam_b_entries = out['fam_b']
    diffs = out['diffs']

    print('# generated by tools.a100_register_report')
    print(f'# source: {dump_path}')
    print('# analysis: Family-A & Family-B non-zero registers + diffs vs cmpunlocker 10GB CMP')
    print()
    print('bar0_dump_summary:')
    print(f'  source_file: {os.path.basename(dump_path)}')
    print(f'  size_bytes: {os.path.getsize(dump_path)}')
    print(f'  family_a_nonzero_count: {len(fam_a_entries)}')
    print(f'  family_b_nonzero_count: {len(fam_b_entries)}')
    print(f'  diffs_from_10gb_cmp: {len(diffs)}')
    print()
    print('# === Family A (0x110000-0x113000) — non-zero registers ===')
    for off, val, cmp_v, differs in fam_a_entries:
        flag = ' ← DIFFERS from 10GB CMP' if differs else ''
        cmp_str = f' (10GB was 0x{cmp_v:08x})' if cmp_v is not None else ''
        marker = '  ← NVIDIA debug marker' if is_marker(val) else ''
        print(f'  - {{addr: 0x{off:06x}, value: 0x{val:08x}}}{cmp_str}{flag}{marker}')
    print()
    print('# === Family B (0x120000-0x123000) — non-zero registers ===')
    for off, val, cmp_v, differs in fam_b_entries:
        flag = ' ← DIFFERS from 10GB CMP' if differs else ''
        cmp_str = f' (10GB was 0x{cmp_v:08x})' if cmp_v is not None else ''
        marker = '  ← NVIDIA debug marker' if is_marker(val) else ''
        print(f'  - {{addr: 0x{off:06x}, value: 0x{val:08x}}}{cmp_str}{flag}{marker}')
    print()
    print('# === cfg1/lmr candidates (sorted by diff-size, biggest diff first) ===')
    diffs_sorted = sorted(diffs, key=lambda d: -abs((d[1] or 0) - (d[2] or 0)))
    for off, val, cmp_v, _ in diffs_sorted:
        if cmp_v is not None:
            print(f'  - {{addr: 0x{off:06x}, value: 0x{val:08x}, 10gb_was: 0x{cmp_v:08x}, delta: 0x{val - cmp_v:+x}}}')


def main(argv=None):
    p = argparse.ArgumentParser(
        prog='a100_register_report',
        description='Static analysis of a 16MB A100 80GB BAR0 dump',
    )
    p.add_argument('dump', help='Path to 16MB raw BAR0 image (e.g. a100-0000_01_00_0-bar0-16m.bin)')
    p.add_argument('--no-yaml', action='store_true',
                   help='Just print human-readable, skip YAML output')
    args = p.parse_args(argv)

    if not os.path.isfile(args.dump):
        sys.exit(f'ERROR: {args.dump} not found')

    data = load_bar0(args.dump)
    if len(data) < 1024 * 1024:
        sys.exit(f'ERROR: dump is only {len(data)} bytes, expected ~16 MB')

    fam_a = dump_range(data, 0x110000, 0x113000, cmp10gb=CMP_10GB_LIVE, label='Family A')
    fam_b = dump_range(data, 0x120000, 0x123000, cmp10gb=CMP_10GB_LIVE, label='Family B')

    # combined diffs (both families)
    diffs = [(o, v, c, d) for o, v, c, d in fam_a + fam_b if d]
    diffs.sort(key=lambda x: x[0])

    out = {'fam_a': fam_a, 'fam_b': fam_b, 'diffs': diffs}

    if not args.no_yaml:
        report_yaml(out, args.dump)
    else:
        print(f'=== Family A non-zero: {len(fam_a)} ===')
        for off, val, _, _ in fam_a:
            print(f'  +0x{off:06x} = 0x{val:08x}')
        print()
        print(f'=== Family B non-zero: {len(fam_b)} ===')
        for off, val, _, _ in fam_b:
            print(f'  +0x{off:06x} = 0x{val:08x}')
        print()
        print(f'=== Diffs vs 10GB CMP: {len(diffs)} ===')
        for off, val, cmp_v, _ in diffs:
            print(f'  +0x{off:06x}: 10GB=0x{cmp_v:08x} → 80GB=0x{val:08x}')


if __name__ == '__main__':
    main()
